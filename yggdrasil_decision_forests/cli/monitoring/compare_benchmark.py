r"""Compares the csv generated by two runs of a benchmark.

Usage example:
  bazel run -c opt \
      //yggdrasil_decision_forests/cli/monitoring:compare_benchmark\
      -- --old=/tmp/train_benchmark_old.csv --new=/tmp/train_benchmark_new.csv
"""

from absl import app
from absl import flags
import pandas as pd

FLAGS = flags.FLAGS

flags.DEFINE_string("old", "", "First csv file")
flags.DEFINE_string("new", "", "Second csv file")


def main(argv) -> None:
  if len(argv) > 1:
    raise app.UsageError("Too many command-line arguments.")

  data = pd.read_csv(FLAGS.old)
  data_new = pd.read_csv(FLAGS.new)
  data["new"] = data_new["training_time"]
  data.rename(columns={"training_time": "old"}, inplace=True)

  data["delta"] = data["new"] - data["old"]
  data["delta%"] = data["delta"] / data["old"]

  with pd.option_context("display.float_format", "{:0.3f}".format):
    print(data)


if __name__ == "__main__":
  app.run(main)
